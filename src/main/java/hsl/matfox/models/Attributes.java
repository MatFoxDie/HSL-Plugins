package hsl.matfox.models;

public class Attributes {
    private int Dmg;
    private int Life;
    private int Def;

    public int getDmg() {
        return Dmg;
    }

    public void setDmg(int dmg) {
        Dmg = dmg;
    }

    public int getLife() {
        return Life;
    }

    public void setLife(int life) {
        Life = life;
    }

    public int getDef() {
        return Def;
    }

    public void setDef(int def) {
        Def = def;
    }
}
